async def prices_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    drivers = get_all_drivers()
    constructors = get_all_constructors()

    drivers_sorted = sorted(drivers, key=lambda d: -d.price)
    constructors_sorted = sorted(constructors, key=lambda c: -c.price)

    lines = ["\U0001f4b0 *\u0426\u0435\u043d\u044b \u043f\u0438\u043b\u043e\u0442\u043e\u0432*\n"]
    lines.append("```")
    for d in drivers_sorted:
        lines.append(f"${d.price:>5.1f}M  {d.name}")
    lines.append("```")

    lines.append("\n\U0001f3d7 *\u0426\u0435\u043d\u044b \u043a\u043e\u043d\u0441\u0442\u0440\u0443\u043a\u0442\u043e\u0440\u043e\u0432*\n")
    lines.append("```")
    for c in constructors_sorted:
        lines.append(f"${c.price:>5.1f}M  {c.name}")
    lines.append("```")

    await update.message.reply_text("\n".join(lines), parse_mode="Markdown")


# ── Menu router for non-conversation buttons ──

async def menu_router(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    text = update.message.text

    if text == MENU_STANDINGS:
        from handlers.standings import standings_command
        await standings_command(update, context)
    elif text == MENU_RACE:
        from handlers.nextrace import nextrace_command
        await nextrace_command(update, context)
    elif text == MENU_PRICES:
        await prices_command(update, context)
    elif text == MENU_RULES:
        await rules_command(update, context)
    elif text == MENU_HELP:
        await help_command(update, context)
    elif text == MENU_CHIPS:
        from handlers.chips import chips_command
        await chips_command(update, context)
    elif text == MENU_SURVIVOR:
        from handlers.survivor import survivor_dm
        await survivor_dm(update, context)


