def _get_db(context: ContextTypes.DEFAULT_TYPE) -> Database:
    return context.bot_data["db"]


async def start_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    db = _get_db(context)

    # Handle deep links
    if context.args:
        arg = context.args[0]
        if arg == "pickteam":
            from handlers.team import pickteam_start
            await pickteam_start(update, context)
            return
        elif arg == "myteam":
            from handlers.team import myteam_command
            await myteam_command(update, context)
            return
        elif arg == "predict":
            from handlers.predict import predict_start
            await predict_start(update, context)
            return
        elif arg == "survivor":
            from handlers.survivor import survivor_dm
            await survivor_dm(update, context)
            return
        elif arg == "chips":
            from handlers.chips import chips_command
            await chips_command(update, context)
            return

    if update.effective_chat.type != "private":
        await update.message.reply_text(
            "\U0001f3ce *F1 Fantasy Bot*\n\n"
            "\u0412\u0441\u0435 \u0443\u0447\u0430\u0441\u0442\u043d\u0438\u043a\u0438 \u0433\u0440\u0443\u043f\u043f\u044b \u043c\u043e\u0433\u0443\u0442 \u043f\u0440\u0438\u0441\u043e\u0435\u0434\u0438\u043d\u0438\u0442\u044c\u0441\u044f \u043a \u043b\u0438\u0433\u0435.\n"
            "\u0418\u0441\u043f\u043e\u043b\u044c\u0437\u0443\u0439 /join \u0434\u043b\u044f \u0440\u0435\u0433\u0438\u0441\u0442\u0440\u0430\u0446\u0438\u0438, /help \u0434\u043b\u044f \u0441\u043f\u0438\u0441\u043a\u0430 \u043a\u043e\u043c\u0430\u043d\u0434.",
            parse_mode="Markdown",
        )
        return

    # DM — register + show dashboard with menu
    user = await db.get_user(update.effective_user.id)
    if not user:
        await db.register_user(
            update.effective_user.id,
            update.effective_user.username,
            update.effective_user.full_name,
        )

    race = await db.get_next_race()
    name = update.effective_user.first_name or update.effective_user.full_name

    lines = [
        "\U0001f3ce *F1 Fantasy League*",
        "\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500\u2500",
    ]

    if race:
        from datetime import datetime
        race_dt = datetime.fromisoformat(race.race_datetime)
        date_str = f"{race_dt.day} {MONTHS_RU.get(race_dt.month, '')} {race_dt.year}"
        lines.append(f"\U0001f3c1 *\u0421\u043b\u0435\u0434\u0443\u044e\u0449\u0430\u044f \u0433\u043e\u043d\u043a\u0430:*")
        lines.append(f"{race.name}")
        lines.append(f"\U0001f4c5 {date_str}")

    lines.append("")
    lines.append(f"\u041f\u0440\u0438\u0432\u0435\u0442, {name}! \U0001f44b")
    lines.append("\u0412\u044b\u0431\u0438\u0440\u0430\u0439 \u0434\u0435\u0439\u0441\u0442\u0432\u0438\u0435 \u0438\u0437 \u043c\u0435\u043d\u044e \U0001f447")

    menu = build_main_menu()
    await update.message.reply_text(
        "\n".join(lines),
        reply_markup=menu,
        parse_mode="Markdown",
    )


async def join_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    db = _get_db(context)
    user_id = update.effective_user.id
    existing = await db.get_user(user_id)

    if existing:
        await update.message.reply_text("\u0422\u044b \u0443\u0436\u0435 \u0432 \u0438\u0433\u0440\u0435! \U0001f44d")
        return

    await db.register_user(
        user_id,
        update.effective_user.username,
        update.effective_user.full_name,
    )

    users = await db.get_all_users()
    count = len(users)
    display = f"@{update.effective_user.username}" if update.effective_user.username else update.effective_user.full_name

    await update.message.reply_text(
        f"\U0001f389 {display} \u0432 \u0438\u0433\u0440\u0435! \u0418\u0433\u0440\u043e\u043a\u043e\u0432: {count}\n\n"
        f"\u0421\u043e\u0431\u0435\u0440\u0438 \u043a\u043e\u043c\u0430\u043d\u0434\u0443: /pickteam",
    )


